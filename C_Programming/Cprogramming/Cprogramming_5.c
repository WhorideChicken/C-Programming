void PointExample()
{
	int* p;
	int a = 2;

	p = &a;

	printf("%p \n", p);
	printf("%d \n", *p);
	printf("%p \n", &a);

	*p = 100;

	printf("%d \n", a);
}


int main()
{
	PointExample();
	return 0;
}
